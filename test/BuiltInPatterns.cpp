bool seedBuiltInPatterns(DatabaseManager& db) {
    bool allSaved = true;

    for (const auto& pattern : getBuiltInPatterns()) {
        const bool saved = db.savePattern(pattern.first, pattern.second);
        allSaved = saved && allSaved;
    }

    return allSaved;
}
